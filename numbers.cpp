#include<iostream>
#include<cmath>
//cmath or math.h includes math function
using namespace std;
int main(){
	cout<<10/3<<endl;
//print 10/3 as 3 
    cout<<22/7.<<endl;
//print 22/7 as 3.14159...
    cout<<pow(3,3)<<endl;
//powers the first argument
    cout<<sqrt(81)<<endl;
//finds square root of a number
    cout<<round(7.51596)<<endl;
//rounds the parameter
    cout<<ceil(3.14159)<<endl;
//force round up
    cout<<floor(7.99)<<endl;
//force round down
    cout<<fmax(299.001,299.0001)<<endl;
//outputs the largest value
    cout<<fmin(23.01,23.05)<<endl;
//outputs the minimum value
}
